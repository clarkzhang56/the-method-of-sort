/*
This bubble sort is used to sort the sequence from low to high.
It's best time complexity is O(n).It's worst time complexity is O(n2).It's average TC is O(n2);
*/
void maopao(int *a, int n){
	int i,j;
	for( i=0; i<n-1; i++){
		for( j=0; j<n-1-i; j++){
			if(a[j] > a[j+1]){
				int tmp = a[j];
				a[j] = a[j+1];
				a[j+1] = tmp;
			}
		}
	}
}
